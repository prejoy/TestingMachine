	if(exsrammalloc == NULL){
		free(addrtoretruen);
		return NULL;
	}


	addrtoretruen->pages = pages;
	addrtoretruen->hTVccSaves.nowaddr = addrtoretruen->hTVccSaves.datastart = (void *)exsrammalloc;
	addrtoretruen->hMVccSaves.nowaddr = addrtoretruen->hMVccSaves.datastart = (void *)((uint32_t)exsrammalloc+MAX_MVCCSAVE_LEN);
