void *addonepage(void *addrcontinue)
{
	void *retaddr=0;
	for (int i=0;i<EXSRAM_PAGENUM;i++)
	{
		if(hexsram.exsramtable[i] == 0)
		{
			retaddr = (void *)(EXSRAM_STARTADDR+i*EXSRAM_PAGESIZE);
			hexsram.exsramtable[i]=1;
			memset(retaddr,0,EXSRAM_PAGESIZE);					//retaddr[2000]开始为page控制区域  +0 4 = next page addr;
			*(uint32_t *)addrcontinue = (uint32_t )retaddr;
			break;
		}
		else
			continue;
	}

	return retaddr;

}

