void releasepages(xExSRAMInstance *pWriteInstance)
{
	int32_t *prelease=0;
	switch(pWriteInstance->dattype)
	{
		case INTTYPE:
		case FLOATTYPE:
			prelease = (int32_t *)pWriteInstance->datastart;
			do{
				hexsram.exsramtable[((uint32_t)prelease&(EXSRAM_LENGTH-1))/2048] = 0;
				prelease = (int32_t *) prelease[(EXSRAM_PAGESIZE/1000*1000)/4];
			}while(prelease!=0 && prelease[(EXSRAM_PAGESIZE/1000*1000)/4]!=0);
			break;
		default:
				break;
	}
}

//快速获取一段连续内存
void *getcontpages(uint32_t requestsize,uint32_t *getpagenum)
{
	void *retaddr=0;
	uint32_t pagerequest;

	pagerequest=(requestsize+(EXSRAM_PAGESIZE/1000*1000)-1)/(EXSRAM_PAGESIZE/1000*1000);
	if(pagerequest == 0)
		return 0;
	for (int i=0;i<EXSRAM_PAGENUM;i++)
	{
		if(hexsram.exsramtable[i] == 0)
		{
			int j=pagerequest-1;
			while(j>0)
			{
				if(hexsram.exsramtable[j+i]==0)
				{
					j--;
					if(j==0){
						*getpagenum = pagerequest;
						retaddr = (void *)(EXSRAM_STARTADDR+i*EXSRAM_PAGESIZE);
						for(int k=i;k<i+pagerequest;k++)
						{
							hexsram.exsramtable[k]=1;
							memset((void *)(EXSRAM_STARTADDR+k*EXSRAM_PAGESIZE),0,EXSRAM_PAGESIZE);
						}
						return retaddr;
					}
				}
				else
				{
					i+=pagerequest-1;
					break;
				}
			}
		}
	}
}

//该函数的pagenum参数一定不能多填
void releasecontpages(void *staddr,uint32_t pagenum)
{
	int index;
	uint32_t startaddr;
	startaddr = (uint32_t)staddr;
	index = (uint32_t)((uint32_t)startaddr-(uint32_t)EXSRAM_STARTADDR)/EXSRAM_PAGESIZE;

	for (int i = 0;i<pagenum;i++)
	{
		hexsram.exsramtable[index+i]=0;
	}
	return;
}


//=========================================              new version                     ====================================
void *getcontinouspages(int32_t pagenum)
{
	if(pagenum<=0 || pagenum>EXSRAM_PAGENUM)
	{
		return NULL;
	}

	for (int i=0;i<EXSRAM_PAGENUM-pagenum;i+=4){	//8K align
		if(hexsram.exsramtable[i]==0){
			int j;
			for(j=0;j<pagenum;j++){
				if((i+j)<EXSRAM_PAGENUM && hexsram.exsramtable[i+j]!=0){
					break;
				}
			}
			if(j<pagenum-1){
				continue;
			}
			else{
				for(;j>=0;j--){
					hexsram.exsramtable[i+j]=1;
				}
				return (void *)(EXSRAM_STARTADDR+EXSRAM_PAGESIZE*i);
			}
		}
	}

	return NULL;
}

void releasecontinouspages(void *addr,int32_t pagenum)
{
	if(pagenum<=0)
		return ;

	uint32_t addrbegin=(uint32_t)addr;

	if(addrbegin<EXSRAM_STARTADDR || addrbegin>=(EXSRAM_STARTADDR+EXSRAM_LENGTH)||addrbegin%EXSRAM_PAGESIZE!=0){
		return ;
	}

	addrbegin-=EXSRAM_STARTADDR;
	for(int i=addrbegin/EXSRAM_PAGESIZE;i<=addrbegin/EXSRAM_PAGESIZE+pagenum;i++){
		if(i < EXSRAM_PAGENUM){
			hexsram.exsramtable[i]=0;
		}else{
			break;
		}
	}
}

void *usermalloc(int32_t size,uint32_t *pages)	//pages 参数用于后续释放使用，需要记录下来
{
	if(size <= 0)
	{
		return NULL;
	}
	*pages = (size+2047)/2048 ;
	return getcontinouspages(*pages);
}

void userfree(void *addr,uint32_t pages)	//pages 参数用于后续释放使用，需要记录下来
{
	releasecontinouspages(addr,pages);
}

