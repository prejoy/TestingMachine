int32_t exsram_release(xExSRAMInstance *pWriteInstance)
{
	void *pNowPageToRelase,*pNextPageToRelase;

	switch(pWriteInstance->dattype){
		case INTTYPE:
		case FLOATTYPE:
			pNowPageToRelase=(int32_t *)pWriteInstance->datastart;
			int32_t* temp = pNowPageToRelase;
			temp += (EXSRAM_PAGESIZE/1000*1000/4);
			pNextPageToRelase = (void *)(*temp);

			while(pNowPageToRelase!=0 && pNowPageToRelase >= (void *)EXSRAM_STARTADDR){
				hexsram.exsramtable[((uint32_t)pNowPageToRelase&(EXSRAM_LENGTH-1))/2048] = 0;
				pNowPageToRelase = pNextPageToRelase;
			}

			break;
	}

	return 0;
}


//主要给电流表使用，涉及到全局结构体变量xEXSRAM_CB
//注意临界保护！！！！！！！！！！！！！！！！！！！！！！！！！！！！！！
void *getpages(uint32_t requestsize)
{
	void *retaddr=0;
	uint32_t *lastpageCB=0;
	uint32_t pagerequest;
	if(requestsize>0)
		pagerequest=(requestsize+(EXSRAM_PAGESIZE/1000*1000)-1)/(EXSRAM_PAGESIZE/1000*1000);
	else
		pagerequest = 1;
	if(pagerequest > hexsram.availablepagenum)
		return 0;
	for (int i=0;i<EXSRAM_PAGENUM;i++)
	{
		if(hexsram.exsramtable[i] == 0)
		{
			retaddr = (void *)(EXSRAM_STARTADDR+i*EXSRAM_PAGESIZE);
			hexsram.exsramtable[i]=1;
			memset(retaddr,0,EXSRAM_PAGESIZE);					//retaddr[2000]开始为page控制区域  +0 4 = next page addr;
			if(lastpageCB != 0)
				*lastpageCB = (uint32_t)retaddr;
			lastpageCB = (uint32_t *)(retaddr);
			lastpageCB+= (EXSRAM_PAGESIZE/1000*1000)/4;
//			*(uint32_t*)(retaddr+EXSRAM_PAGESIZE/1000*1000) = 0;
			pagerequest--;
			if(pagerequest == 0)
				break;
		}
		else
			continue;
	}

	return retaddr;
}

