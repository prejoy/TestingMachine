int32_t exsram_write(xExSRAMInstance *pWriteInstance,void *dat,int32_t len)
{
	int32_t i=0;

	if(len < 0)
		return 0;
	if(pWriteInstance->datastart == 0)		//== NULL   init
	{
		pWriteInstance->datastart = getpages(len);
		pWriteInstance->len = 0;
		pWriteInstance->nowaddr = pWriteInstance->datastart;
	}

	switch(pWriteInstance->dattype)
	{
		case INTTYPE:
			for(i=0;i<len;i++)
			{
				if((++pWriteInstance->len) % (EXSRAM_PAGESIZE/1000*1000/4) == 0)
				{
					int32_t *temppointer=(int32_t *)pWriteInstance->nowaddr;
					if(*(++temppointer)!=0)									//注意越过一页时的判断
						pWriteInstance->nowaddr = (int32_t *)(*temppointer);
					else
					{
						pWriteInstance->nowaddr = addonepage((void *)temppointer);
						if(pWriteInstance->nowaddr == 0){
							assert_failed(__FILE__,__LINE__);
							return 1;
						}
					}
				}

				int32_t *ptowrite = (int32_t *)pWriteInstance->nowaddr;
				int32_t *ptocopy = (int32_t *)(dat);
				ptocopy +=i;

				*ptowrite = *ptocopy;
				ptowrite++;
				pWriteInstance->nowaddr = (int32_t *)ptowrite;
				//pWriteInstance->len;
			}
			break;
		case FLOATTYPE:
			for(i=0;i<len;i++)
			{
				if((++pWriteInstance->len) % (EXSRAM_PAGESIZE/1000*1000/4) == 0)
				{
					int32_t *temppointer=(int32_t *)pWriteInstance->nowaddr;
					if(*(++temppointer)!=0)									//注意越过一页时的判断
						pWriteInstance->nowaddr = (int32_t *)(*temppointer);
					else
					{
						pWriteInstance->nowaddr = addonepage((void *)temppointer);
						if(pWriteInstance->nowaddr == 0){
							assert_failed(__FILE__,__LINE__);
							return 1;
						}
					}
				}

				float *ptowrite = (float *)pWriteInstance->nowaddr;
				float *ptocopy = (float *)(dat);
				ptocopy +=i;

				*ptowrite = *ptocopy;
				ptowrite++;
				pWriteInstance->nowaddr = (float *)ptowrite;
				//pWriteInstance->len;
			}
			break;
		default:
			break;

	}

	return 0;
}


