void HAL_UART_ErrorCallback(UART_HandleTypeDef *huart)
{
	if(huart == &WifiPort)
	{
		switch(huart->ErrorCode){
			case HAL_UART_ERROR_DMA:
				if(huart->hdmatx->ErrorCode>0){
					assert_failed("dma tx error",huart->hdmatx->ErrorCode);
				}else if(huart->hdmarx->ErrorCode>0){
					assert_failed("dma rx error",huart->hdmarx->ErrorCode);
				}
				break;
			default:
				break;
		}
	}
	else 	if(huart == &ModulePort)
	{
		assert_failed("\r\n module port uart error\r\n",0);
	}
	else	if(huart == &CurrentPort)
	{
		assert_failed("\r\n current port uart error\r\n",0);
	}
}

